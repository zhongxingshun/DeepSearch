const detectInitialLocale = (): Locale => {
  const saved = localStorage.getItem(STORAGE_KEY)
  if (saved === 'zh-CN' || saved === 'en-US') {
    return saved
  }

  const browserLanguage = navigator.language.toLowerCase()
  return browserLanguage.startsWith('en') ? 'en-US' : 'zh-CN'
}

const locale = ref<Locale>(detectInitialLocale())

const getValue = (target: Record<string, any>, path: string): unknown => {
  return path.split('.').reduce<unknown>((acc, key) => {
    if (acc && typeof acc === 'object' && key in (acc as Record<string, unknown>)) {
      return (acc as Record<string, unknown>)[key]
    }
    return undefined
  }, target)
}

const interpolate = (template: string, params?: Record<string, string | number>) => {
  if (!params) return template
  return template.replace(/\{(\w+)\}/g, (_, key) => String(params[key] ?? ''))
}

export const setLocale = (nextLocale: Locale) => {
  locale.value = nextLocale
  localStorage.setItem(STORAGE_KEY, nextLocale)
  document.documentElement.lang = nextLocale
}

export const t = (path: string, params?: Record<string, string | number>) => {
  const raw =
    getValue(messages[locale.value] as Record<string, any>, path) ??
    getValue(messages['zh-CN'] as Record<string, any>, path) ??
    path

  return typeof raw === 'string' ? interpolate(raw, params) : path
}

document.documentElement.lang = locale.value

export const elementLocale = computed(() => (locale.value === 'en-US' ? en : zhCn))

export const useI18n = () => ({
  locale,
  setLocale,
  t,
  isEnglish: computed(() => locale.value === 'en-US'),
})
